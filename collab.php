  <h3>Collaborating as a Style</h3>
  <p>Your frequent use of collaborating may also be part of a collaborating style you have developed to deal with conflict. Styles are rooted in personal beliefs, values, and motives that "push" one's conflict behavior in a consistent direction.</p>
  <p>Collaborators tend to see conflicts as problems to be solved, wanting quality decisions that truly resolve the issues. They believe in the power of consensus and in sharing information and understandings. They regard teammates as allies and tend to see people outside the team as potential allies. They build on others' ideas and listen well. Collaborators value innovation, open-mindedness, learning, and consensus. They look for the value in what others say and combine that with their own insights to find win-win solutions.</p>
  <h3>Contributions of a Collaborating Style</h3>
  <p>Collaborators are helpful in reaching win-win solutions that provide a long-term resolution to a conflict issue. They ask questions, listen to other points of view, and try to incorporate those viewpoints into a richer, shared understanding. In the process, they aid open communication and learning. Often, collaborators are able to find superior, high-quality solutions to important issues. They may be sources of creativity and innovation. They help people air diverse views without putting anyone on the defensive.</p>
