<h1>The Five Conflict-Handling Modes</h1>
  <p>The Thomas-Kilmann Conflict Modes Instrument (TKI) assesses an individual’s behavior in conflict situations—that is, situations in which the concerns of two people appear to be incompatible. In conflict situations, we can describe a person’s behavior along two basic dimensions:</p>
  <ol>
    <li><strong>Assertiveness:</strong> The extent to which the individual attempts to satisfy their own concerns.</li>
    <li><strong>Cooperativeness:</strong> The extent to which the individual attempts to satisfy the concerns of others.</li>
  </ol>
  <p>These two dimensions of behavior can be used to define five methods of dealing with conflict. These five conflict-handling modes are shown below:</p>
  <ul>
    <li>Competing</li>
    <li>Collaborating</li>
    <li>Compromising</li>
    <li>Avoiding</li>
    <li>Accommodating</li>
</ul>

<h3>COMPETING</h3>
  <p>Competing is assertive and uncooperative, a power-oriented mode. When competing, an individual pursues his or her own concerns at the other person's expense, using whatever power seems appropriate to win his or her position. Competing might mean standing up for your rights, defending a position you believe is correct, or simply trying to win.</p>
  
  <h3>COLLABORATING</h3>
  <p>Collaborating is both assertive and cooperative. When collaborating, an individual attempts to work with the other person to find a solution that fully satisfies the concerns of both. It involves digging into an issue to identify the underlying concerns of the two individuals and to find an alternative that meets both sets of concerns. Collaborating between two persons might take the form of exploring a disagreement to learn from each other's insights, resolving some condition that would otherwise have them competing for resources, or confronting and trying to find a creative solution to an interpersonal problem.</p>
  
  <h3>COMPROMISING</h3>
  <p>Compromising is intermediate in both assertiveness and cooperativeness. When compromising, the objective is to find an expedient, mutually acceptable solution that partially satisfies both parties. Compromising falls on a middle ground between competing and accommodating, giving up more than competing but less than accommodating. Likewise, it addresses an issue more directly than avoiding but doesn't explore it in as much depth as collaborating. Compromising might mean splitting the difference, exchanging concessions, or seeking a quick middle-ground position.</p>
  
  <h3>AVOIDING</h3>
  <p>Avoiding is unassertive and uncooperative. When avoiding, an individual does not immediately pursue his or her own concerns or those of the other person. He or she does not address the conflict. Avoiding might take the form of diplomatically sidestepping an issue, postponing an issue until a better time, or simply withdrawing from a threatening situation.</p>
  
  <h3>ACCOMMODATING</h3>
  <p>Accommodating is unassertive and cooperative-the opposite of competing. When accommodating, an individual neglects his or her own concerns to satisfy the concerns of the other person; there is an element of self-sacrifice in this mode. Accommodating might take the form of selfless generosity or charity, obeying another person's order when you would prefer not to, or yielding to another's point of view.</p>

