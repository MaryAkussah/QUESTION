  <h3>Interpreting Your Scores</h3>
  <p>When you look at your profile on the TKI, you probably want to know, "What are the correct answers?" In the case of conflict-handling behavior, there are no right or wrong answers. All five modes are useful in some situations: each represents a set of useful social skills. Our conventional wisdom recognizes, for example, that often "Two heads are better than one" (collaborating). But it also says, "Kill your enemies with kindness" (accommodating), "Split the difference" (compromising), "Leave well enough alone" (avoiding), and "Might makes right" (competing). The effectiveness of a given conflict-handling mode depends on the requirements of the specific situation and the skill with which you use that mode.</p>
  <p>You are capable of using all five conflict-handling modes; you cannot be characterized as having a single, rigid style of dealing with conflict. However, most people use some modes more readily than others, develop more skills in those modes, and therefore tend to rely on them more heavily. Many have a clear favorite. The conflict behaviors you use are the result of both your personal predispositions and the requirements of the situations in which you find yourself.</p>
